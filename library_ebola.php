        <p>Some people with the illness may bleed from the eyes, nose, ears, and rectum. Lab tests may reveal abnormal liver and kidney function. Levels of white blood cells and platelets may be elevated. The incubation period of Ebola, defined as the period of time from exposure to the virus until the onset of symptoms, is 21 days. The average time from exposure to symptom development is about 8 – 10 days; bleeding is usually a later symptom that signifies severe infection.</p>
