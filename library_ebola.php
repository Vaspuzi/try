          <h2><span>Ebola</span> Disease</h2>
          <div class="clr"></div>
          <p><strong>Ebola virus disease (EVD)</strong>, also known as <strong>Ebola hemorrhagic fever (EHF)</strong> or simply <i><b>Ebola</b></i>, is a viral hemorrhagic fever of humans and other primates caused by ebolaviruses. Signs and symptoms typically start between two days and three weeks after contracting the virus with a fever, sore throat, muscular pain, and headaches. Then, vomiting, diarrhea and rash usually follow, along with decreased function of the liver and kidneys. At this time some people begin to bleed both internally and externally. The disease has a high risk of death, killing between 25 and 90 percent of those infected, with an average of about 50 percent. This is often due to low blood pressure from fluid loss, and typically follows six to sixteen days after symptoms appear.</p>
          <p>The virus spreads by direct contact with body fluids, such as blood, of an infected human or other animals.[1] This may also occur through contact with an item recently contaminated with bodily fluids. Spread of the disease through the air between primates, including humans, has not been documented in either laboratory or natural conditions.[3] Semen or breast milk of a person after recovery from EVD may carry the virus for several weeks to months. Fruit bats are believed to be the normal carrier in nature, able to spread the virus without being affected by it. Other diseases such as malaria, cholera, typhoid fever, meningitis and other viral hemorrhagic fevers may resemble EVD. Blood samples are tested for viral RNA, viral antibodies or for the virus itself to confirm the diagnosis.</p>
          <p>Control of outbreaks requires coordinated medical services, alongside a certain level of community engagement. The medical services include rapid detection of cases of disease, contact tracing of those who have come into contact with infected individuals, quick access to laboratory services, proper healthcare for those who are infected, and proper disposal of the dead through cremation or burial. Samples of body fluids and tissues from people with the disease should be handled with special caution. Prevention includes limiting the spread of disease from infected animals to humans. This may be done by handling potentially infected bushmeat only while wearing protective clothing and by thoroughly cooking it before eating it. It also includes wearing proper protective clothing and washing hands when around a person with the disease. No specific treatment or vaccine for the virus is available, although a number of potential treatments are being studied. Supportive efforts, however, improve outcomes. This includes either oral rehydration therapy (drinking slightly sweetened and salty water) or giving intravenous fluids as well as treating symptoms.</p>
          <p>The disease was first identified in 1976 in two simultaneous outbreaks, one in Nzara, and the other in Yambuku, a village near the Ebola River from which the disease takes its name. EVD outbreaks occur intermittently in tropical regions of sub-Saharan Africa.[1] Between 1976 and 2013, the World Health Organization reports a total of 24 outbreaks involving 1,716 cases. The largest outbreak to date was the epidemic in West Africa, which occurred from December 2013 to January 2016 with 28,616 cases and 11,310 deaths. It was declared no longer an emergency on 29 March 2016.</p>
