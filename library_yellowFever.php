          <h2><span>Yellow</span>Fever</h2>
          <div class="clr"></div>
          <p>
<b>Yellow fever</b> is a viral disease of typically short duration. In most cases, symptoms include fever, chills, loss of appetite, nausea, muscle pains particularly in the back, and headaches. Symptoms typically improve within five days. In about 15% of people within a day of improving, the fever comes back, abdominal pain occurs, and liver damage begins causing yellow skin. If this occurs, the risk of bleeding and kidney problems is also increased.
The disease is caused by the <b>yellow fever virus</b> and is spread by the bite of an <b>infected female mosquito</b>. It infects only humans, other primates, and several species of mosquitoes. In cities, it is spread primarily by Aedes <b>aegypti</b>, a type of mosquito found throughout the tropics and subtropics. The virus is an RNA virus of the genus <b>Flavivirus</b>. The disease may be difficult to tell apart from other illnesses, especially in the early stages. To confirm a suspected case, blood sample testing with polymerase chain reaction is required.
A safe and effective vaccine against yellow fever exists and some countries require vaccinations for travelers. Other efforts to prevent infection include reducing the population of the transmitting mosquito. In areas where yellow fever is common and vaccination is uncommon, early diagnosis of cases and immunization of large parts of the population is important to prevent outbreaks. Once infected, management is symptomatic with no specific measures effective against the virus.[ Death occurs in up to half of those who get severe disease.
In 2013, <b>yellow fever</b> resulted in about 127,000 severe infections and 45,000 deaths, with nearly 90% of these occurring in Africa. Nearly a billion people live in an area of the world where the disease is common. It is common in tropical areas of <b>South America</b> and <b>Africa</b>, but not in <b>Asia</b>. Since the 1980s, the number of cases of <b>yellow fever</b> has been increasing. This is believed to be due to fewer people being immune, more people living in cities, people moving frequently, and changing climate. The disease originated in <b>Africa</b>,
 from where it spread to <b>South America</b> through the slave trade in the 17th century.
Since the 17th century, several major outbreaks of the disease have occurred in the <b>Americas, Africa,</b> and <b>Europe</b>. In the 18th and 19th centuries, <b>yellow fever</b> was seen as one of the most dangerous infectious diseases.
 In 1927 yellow fever virus became the first human virus to be isolated.
          </p>

