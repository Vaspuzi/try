        <h2 id="trans"><strong>Transmission<span></span></strong></h2>
        <div class="clr"></div>
        <p>It is believed that between people, Ebola disease spreads only by direct contact with the blood or other body fluids of a person who has developed symptoms of the disease. Body fluids that may contain Ebola viruses include saliva, mucus, vomit, feces, sweat, tears, breast milk, urine and semen. The WHO states that only people who are very sick are able to spread Ebola disease in saliva, and whole virus has not been reported to be transmitted through sweat. Most people spread the virus through blood, feces and vomit. Entry points for the virus include the nose, mouth, eyes, open wounds, cuts and abrasions. Ebola may be spread through large droplets; however, this is believed to occur only when a person is very sick. This contamination can happen if a person is splashed with droplets. Contact with surfaces or objects contaminated by the virus, particularly needles and syringes, may also transmit the infection. The virus is able to survive on objects for a few hours in a dried state, and can survive for a few days within body fluids outside of a person.</p>

