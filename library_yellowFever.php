        <h2 id="treat"><strong>Treatment<span></span></strong></h2>
        <div class="clr"></div>
        <p>
As for other flavivirus infections, <b>no cure is known for yellow fever</b>.
Hospitalization is advisable and intensive care may be necessary because of rapid deterioration in some cases. Different methods for acute treatment of the disease have been shown not to be very successful; passive immunisation after emergence of symptoms is probably without effect.
<strong>Ribavirin</strong> and other antiviral drugs, as well as treatment with interferons, do not have a positive effect in patients. A symptomatic treatment includes rehydration and pain relief with drugs such as paracetamol (acetaminophen in the United States). <b>Acetylsalicylic acid (aspirin)</b> should not be given because of its <b>anticoagulant effect</b>,
which can be devastating in the case of internal bleeding that can occur with yellow fever.
        </p>

